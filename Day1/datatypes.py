# integer type
age = 21
print(type(age))

# Float type
weight = 65.5
print(type(weight))

# complex type

z = 5 + 4j
print(type(z))

# string type

firstName = "John"
secondName = 'Doe'
print(type(firstName))
print(type(secondName))
print("It's me John Doe")

print(firstName + secondName)
print(firstName.lower())

# Boolean
# True  False

isMale = True
isFemale = False

print(type(isMale))
print(isMale == isFemale)